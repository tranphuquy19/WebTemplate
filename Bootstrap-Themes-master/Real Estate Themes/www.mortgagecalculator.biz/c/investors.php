               <h3>Where to Start</h3>
<p><img src="../img/golden-temple.jpg" width="630" height="380" alt="Golden Temple." /></p>
<p>Getting into real estate investing is basically about buying property   for the sole purpose of profiting from it later. It is a great way to   make extra money. It can also become an abundant source of income and   yield you nice tax incentives. </p>
<p>
  Generally, when people decide to purchase a home, they are considered to   be making an investment.  This is typically because most people   nowadays do not buy property to live in it forever. People move around   after a while. When one decides to sell their home after building   equity, it is desirable for the homeowner to make a profit by selling it   at a much higher value than it was originally purchased.</p>
<p>
  Aside from making a profit from selling your home, investing in other   properties is a serious business for those who are ready to commit to   the exciting challenge. Though investing in real estate can be   lucrative, it should not be pursued as a way to get rich quick. It   requires time (even years), problem solving and diligence.</p>
<p>
  To become successful at real estate investing, start off with learning   the basics, and know what steps you need to take along the way. Then   familiarize yourself with the things you should avoid to prevent money   loss or any other setbacks. This article will provide some helpful tips   to get you stated.</p>
<h3>Steps to Take as a New Investor</h3>
 <p> So, you want to start investing in real estate? Here are some important   steps to take as you begin to transform yourself into an investor.</p>
<h4>Get Educated</h4>
  <p>
  The first and most important step when pursing real estate investing for   the first time is to get educated. Take classes in real estate   investing. Get books, and research the different topics that you are   unsure about. Become very acquainted with ethics and the laws in your   state or the state in which you are planning to invest property. To go a   step further, find a trusted mentor or a real estate coach to help   guide you through your first investment endeavor. </p>
<p>
  Other Important Things to Know:</p>
<ul class="arrow">
  <li>
    	Learn when are the best times to buy or sell.</li>
  <li> Learn negotiation techniques.</li>
  <li> Learn the real estate terms and jargon.</li>
  <li> Learn about different insurance options.</li>
</ul>
<h4>Start Building Equity</h4>
  <p>
  The longer you stay in a home, the more equity you can acquire (the   actual value that you get to pocket after you pay off the bank). Unless   you can afford to purchase property in one lump sum, it's a good   practice to pay a little bit more than what is required for your monthly   mortgage. This will decrease the amount you have to pay in interest,   and speed up the time when you finally complete your mortgage payments.</p>
  <p>Those who reside in a home for a while and build equity can make a   decent return when they decide to put the property up for sale.</p>
<h3>Develop a Plan</h3>
  <p>
  Have an idea of what kind of property (residential or commercial) you   are interested in purchasing for investment purposes. In addition, know   how you plan to make your profit.</p>
<p>
  Go after one investment project at a time. Investing in property may   seem simple and exciting, but you do not want to just dive in without   testing the waters first. You want to make sure that you don't take on   more than you can handle at one time. Get a feel for investing, and see   if it is right for you. After your first success, you can go on to   pursue the next.</p>
<p>
  Investing in single family homes, apartment or condo units would most   likely be easier to invest in than larger complexes.  Once an investor   becomes more advanced, investing on grander scale becomes more ideal. In   addition, business or commercial properties can yield a desirable   profit as well. </p>
<p>
  There are different ways to invest in property. After you buy a house or   other type of real estate, you can make a profit by doing the following   things:</p>
<ul class="arrow">
  <li>
    	Use/live in the property for some time, build equity and make a profit in later years to come.</li>
  <li> Share and significantly reduce your mortgage payments by renting out a   space within your residence or office building. For example, one may   choose to rent out a basement apartment, back house or other space.</li>
  <li> Purchase property solely to rent out to tenants.</li>
  <li> Purchase property and provide a Lease-to-Own option. Here, a   percentage of the monthly payments go towards the purchase price once an   individual decides to take over the mortgage. </li>
  <li> Resell a property to another investor for a higher profit.</li>
</ul>
<h4>
  Budget</h4>
<p>
  Another huge part of developing your plan has to be figuring out your   budget. You should know just how much money you are investing. This   amount is not only referring to the money used to purchase the property.   Other expenses can include money spent on renovation projects or hired   professionals such as a home inspector and etc. </p>
<p>Have a set amount that you want to spend, and keep track of your budget.   Basically, be conscious of how much you have spent, how much you are   bringing in and how much you plan on saving for each month. </p>
<h4>
  Rehabbing Properties: Renovations and Updates to Increase Your Profit </h4>
<p>
  Rehabbing should be a part of your plan if you want to make the most of   your investment. A great way to increase the property value before you   choose to sell, lease, or rent it out is by doing renovations and   upgrades.</p>
<p>
  When choosing to make changes to a property, consider the amount that   has to go in to completing each project. Depending on the work that   needs to be done, you may have to hire outside professionals. </p>
<p>
  You don't want to lose money by flipping a house that doesn't bring in a   favorable return. It's better to purchase property under value if you   already know you want to do some renovations. However, be aware that you   may be responsible for income taxes for the money you make from doing   the rehabs. </p>
<p>
  Here are examples of projects that attract buyers, stable tenants and raise the property value.</p>
<ul class="arrow">
  <li>
    	Replacing Windows and Doors</li>
  <li> Updating the Kitchen (new cabinets, counter tops and appliances)</li>
  <li> Modernizing the Bathrooms</li>
  <li> Redoing the Floors (carpet, tile or hardwood floors)</li>
  <li> Finishing Basements </li>
  <li> Developing the Yard and Creating Curb Appeal</li>
</ul>
<h4>Know How Much Money You Need to Start</h4>
  <p>
  You definitely need to save up enough money to cover your purchase   price. Yet knowing how much you need to get started really depends on a   few other factors. It obviously depends on your particular budget and   plan. However, the amount may change for the different region/community   you are planning to invest in, the property taxes and the current   property value. </p>
<p>
  Keep in mind that when buying property, you want to put down more than   10% to obtain lower fees and interest rates. Since recent changes in the   market, a lot of places actually expect around 20% down instead. </p>
<p>
  If you are renting out a property, then you should also account for   partial utility payments, maintenance fees, the mortgage payment and   taxes on rent. In addition, remember to consideration the cost you will   need for any renovations or outside assistance.</p>
<h3>Common Pitfalls For New Investors (and How to Avoid Them)</h3>
<h4>Failing to Stick to a Plan and a Budget</h4>
  <p>
  Many investors have a setback by not sticking to their plans and fixed   budget. Know the amount of money you want to spend on each property.   Don't pay too much for a property and end up not making the return that   you were expecting. Use negotiation techniques if you need to. Just   stick to your budget. Don't make decisions on an impulse, and keep a   record of all your spending.</p>
  <p>Staying organized will help ensure that money does not get mismanaged.</p>
  <p>Also, be wise when selecting the rent price or resell value of your properties.</p>
<h4>Trying to Do it All by Themselves</h4>
  <p>
  It's okay to have help. Gather a team for support and establish a   network of professionals within the business. Share tips, clients and   resources. Close on a deal much faster and smoother with a trusted team.</p>
<p>
  Here's a list of professionals you may want to have in your network.</p>
<ul class="arrow">
  <li>
    	Property Manager</li>
  <li> Real Estate Agent</li>
  <li> Tax Advisor or Lawyer</li>
  <li> Loan Officer</li>
</ul>
<p>
  In addition, consider joining associations just for real estate investors.</p>
<h4>Not Properly Educating Themselves</h4>
  <p>
  Taking the time to read over materials, take classes and learn from   others is a great investment of your time. Staying knowledgeable and   ahead of the game will give you an advantage that you don't want to miss   out on.</p>
<h4>Choose the Wrong Property</h4>
  <p>
  Unfortunately, investors sometimes lose money when they unknowingly   invest in the wrong property. However, there are ways to avoid this   mishap. </p>
<p>
  It is important to know what the property is really worth before you buy   it. If you choose to flip a property, make sure it is worth it. Inquire   about why a particular place is up for sale. It's also a good idea to   hire a home inspector. There may be issues within the house that may not   have been brought to your attention. For instance, there may be   plumbing, insulation or electrical problems.</p>
<p>
  Another way of preventing this pitfall is by carefully selecting the   right location. Consider looking for newer developments. Choose a   location that will be within a fair distance so that you can get to it   when needed. For instance, a property 2 Â½ hours away may be too far to   manage. Also, certain locations are harder to land a sale. </p>
<p>
  Investing in real estate is an excellent way to bring in extra money and   can even turn into a lucrative career. It takes a skill and diligence.   Following the tips above will help to get you on your way.</p>
