               <h3>Selling Your Home Wtihout Those Saddening Expenses</h3>
<p><strong>A Comprehensive Home Seller's Guide on How to Save Money While Selling Your Home</strong></p>
<p><img src="../img/new-listing.jpg" width="630" height="373" alt="New Listing." /></p>
<p>When selling your home, you want to squeeze as much value as you can out   of it, but how? For inexperienced sellers, moving a house can seem to   be a monumental task filled with confusing jargon, complicated   paperwork, frustrating response levels, and unexpected, highly costly   expenses. That last issue, the expenses that come with selling a home,   are the biggest concern that sellers have when trying to stamp the words   SOLD in big bold letters over top of their for sale sign.</p>
<p>
  There are a number of different factors to consider when trying to save   money while selling your house, including deciding whether you want to   hire an agent or sell the house yourself, the best and most inexpensive   marketing techniques, how to save on legal matters, and how to make sure   your home is ready to sell without overspending. Knowing which routes   to take and how to approach them will make your head spin if you do not   know where to start, and that is why we are providing you with a   comprehensive guide to save every penny that you can when putting your   home up for sale. Our guide includes a list of each possible road to   follow; with our detailed breakdowns on how to save the most money   possible for each one will make traversing that path seem a little less   lonely.</p>
<h2>Rout Number One: Go the For Sale By Owner (FSBO) Way</h2>
<p>
  The biggest issue with hiring a real estate agent is simple:   professional real estate agents take a commission. Their commissions   usually hover at around 6 percent of the total sale, and while that can   seem like a paltry investment, keep in mind that is a chunk coming out   of your total sale price. In other words, if you are selling your home   for $200,000, you have just given away twelve thousand dollars. Was it   worth it? Probably not. As complicated as real estate agents want you to   think it is, the process of selling your home is a lot easier than you   think—if you know the basics. Below are the simple steps that you need   to be well on your way to becoming your own real estate agent:</p>
<h3>
  FSBO Step Number 1: Put Your Home on the Market</h3>
<p>
  This may seem like a no-brainer, but it is an often mishandled aspect of   do it yourself home selling. Many DIY home sellers think that by   sticking a for sale sign into the dirt they have adequately announced to   the world that their home is for sale to the highest bidder. While you   may think that your house has a prime location that is sure to attract   dozens of buyers, remember this: we live in a day and age where people   can go to work and go grocery shopping without leaving the comfort of   their beds; a for sale by owner sign is nowhere near enough.</p>
<p>
  With fewer people going outside to browse the streets for their next   home buy—or anything else—you may be wondering how to announce your   home's availability to the real estate marketplace. The answer to that   question, you need to look to the same places that people are doing   their simultaneous working and grocery shopping. No, not their beds; the   internet is where you want to go. In most cases, the worldwide web will   in fact be the best resource available to you throughout the home   selling process.</p>
<p>
  The internet is a great resource, but it is massive, and like anything   of that magnitude, it is great to know where to start. When posting your   home to the web you want to use high-traffic real estate communities,   such as Zillow.com, Trulia.com, and ZipRealty.com; even online   marketplaces like craigslist.com and ebay.com are increasingly popular   places to make homes available for sale. A quick internet search of the   words &ldquo;home for sale&rdquo; should bring up these and countless other sites to   post to; most of them make their money through advertising, so you   don't have to worry about paying a dime.</p>
<p>
  Newspapers can also be a fairly effective method of getting the word   out, but be mindful that they DO charge by the word for each issue that   they post your FSBO notice in, so if you aren't getting much   responsiveness from running newspaper ads, cancel them.</p>
<h3>
  FSBO Step Number 2: Make Sure the Price is Right</h3>
<p>
  Listing your home at an optimal price is unfathomably important. List   your house to high, and no prospective buyers will bite; too low and you   could be costing yourself tens of thousands of dollars. That is why you   must know your home's market value; while knowing this may seem tricky,   it can be made awfully simple by knowing a few basic things. First, you   will want to visit those real estate listing sites, because most of   them provide details about recent sales. In order to obtain more details   about the recent sales in the area, you can visit your county's public   records website. All property sales are made public record by law, and   most county public records websites allow anybody to search for recent   sales by zip code, neighborhood, and address.</p>
<p>
  Comparing your home with houses in the area will allow you to better   gauge your home's value, but don't just try to match the prices that you   see other properties being sold for. If a nearby house similar to yours   is sold for $200,000, but you have a swimming pool and they don't, add   about ten grand to your home's value; the same goes for number of rooms,   bathrooms, square footage, property area, ect. Use anything that you   think might add value to your home to leverage a few more dollars into   the listing price. Remember, though that the inverse rule applies if   your home is missing features that a recently sold house possesses. Once   you know what your desired selling price is, list your home at a price   about five percent higher, because buyers are going to want to negotiate   anyway; if you get lucky, someone might just go for your listing price;   otherwise, let them talk you down to your preferred selling price.</p>
<p>
  Also remember that the market is not static, and neither is your listing   price. If the overall market drops, make an assessment to see if you   need to lower your price; if the market spikes, don't be afraid to slap a   few grand onto your asking price.</p>
<h3>
  FSBO Step Number 3: Market your Home</h3>
<p>
  Now that the word is out that you want to sell your lovely three bedroom   townhome for $200,000, it is time to let everyone know why your home is   a better deal than the identical one down the street that is selling   for the same price.</p>
<p>
  There are countless marketing tactics, and some of them can be costly.   Many people think that a good marketing plan involves big bucks, but   smarter, more cost-conscious marketing plans are always more effective   than plans that mostly involve tossing dollars down a black hole without   really knowing if you are getting reasonable returns. Below are some of   the best and most cost-effective ways to market your home:</p>
<ul class="arrow">
  <li><strong>FSBO Lawn Signs:</strong> Although many people no longer get out enough to come across FSBO   signs, there are those who do; some people interested in buying a new   house will even drive around searching for FSBO signs.</li>
  <li><strong>Post Positive Details about your House on those Online Listing Sites:</strong> People scanning the web for a home look at prices first, but pricing   is so strongly guided by the market that they look for something to set   the homes they click on apart. Be creative, and list every positive   element of your house in vibrant, but concise detail; be sure not to   over embellish though. Being truthful will go a long way in closing on   potential deals that you attract.</li>
  <li><strong>Photograph Everything!</strong> When someone is searching for a new home, they want to be able to   picture themselves living there. By photographing every nook and cranny   of your house—free of clutter—you are sure to get about a dozen photos   that will allow prospective buyers to visualize themselves in your home.</li>
  <li><strong> Create Catchy Headlines</strong>
<ul class="arrow">
      <li>When posting to an EBay or Craigslist, or putting an ad in the   newspaper, you are competing with countless other sellers who all want   to be noticed just as much as you do; there are so many that most home   shoppers won't give an advertisement more than a few seconds to entice   them. This is why a concise, catchy headline will go a long way in   attracting prospective buyers.</li>
      <li> Highlight as many positive attributes about your home as you can in   only a few words. If you think your house is worth more than what you   are selling it for, try &ldquo;Selling Below Market Value&rdquo;. If your home is   conveniently located near the metro train station, write &ldquo;Conveniently   Located Two Blocks from Subway&rdquo;. Heck, try to rhyme if you can. Get   creative. Others will try to be.</li>
    </ul>
  </li>
  <li><strong>Hold an Open House or Two</strong>
<ul class="arrow">
      <li>This will help get the word out to people in the local area that you   are selling your house; even if the people that attend aren't really   interested, this will help spread the word.</li>
      <li> Make sure your house is CLEAN when you hold an open house. And by   clean, we don't mean tidy. Clear out the living room, the bedrooms and   the basement. Create as much open space as possible. People are coming   to see your property so that they can visualize living there in their   own style, not to see how well you can decorate.</li>
    </ul>
  </li>
</ul>
<p>
  Following these tips will put you well on your way to mass marketing your home.</p>
<h3>
  FSBO Step Number 4: Cover your Legal Bases</h3>
<p>
  Hiring a lawyer usually only costs a few hundred dollars, and they will   make sure that you do not make a legal mistake that could cost you tens   of thousands of dollars. Before you to sign or agree to anything,   consult a lawyer, and if at all possible, have a lawyer that YOU hired   present at all steps of a transaction.</p>
<h3>
  FSBO Step Number 5: Find out How the Buyer Intends to Pay</h3>
<p>
  Once someone has contacted you about your home, it is prudent to fund   out how they intend to pay. A buyer paying cash, for instance is a sure   bet. Buyers that are pre-approved for a mortgage are usually pretty   certain as well. Watch out for buyers who haven't been approved for a   mortgage, or who won't specify how they intend to pay. These people are   likely to end up wasting your time.</p>
<h3>
  FSBO Step Number 6: Negotiate a Price that Works for You</h3>
<p>
  Now that prospective buyers are banging on your door, and you have a   lawyer covering your backside, it is time to begin the negotiation   process. When in the midst of a negotiation, a smart buyer is going to   try and convince you that it is a good idea that you should sell your   home well below its value; when the negotiation process starts, each   side is trying to sell the other on a sale price.  Don't let them suck   you in to a price that you don't feel comfortable with. Stand firm and   keep hitting them with counteroffers; if they don't like your final   offer, you should have marketed well enough that they aren't the only   one interested, so move on.</p>
<p>
  Keep in mind that offers and counteroffers are usually made in the form   of a binding contract if both sides agree, so don't make multiple   counteroffers, because you could be in serious legal trouble if multiple   buyers agree to your counteroffers—your lawyer should advise you of   this.</p>
<h3>
  FSBO Step Number 7: Closing</h3>
<p>
  Now that you have nailed down a buyer at an agreeable price, you can   just sit back and wait for closing right? Absolutely not. Once a   contract is signed, most buyers will require a home inspection period to   determine if any major repairs are needed to bring the home up to par;   if there are, the contract could be voided, so it would behoove you to   find and fix and broken water pipes, roof leaks, or termite infestations   that you find.</p>
<h3>
  Time to go Sell</h3>
<p>
  Now that you are an FSBO guru, it's time to flex that newfound knowledge   and move your home with most of the money going into your pocket.</p>

<h2>Route Number Two: Playing Hardball With Real Estate Agents </h2>
<p>If you simply do not have the time to sell your house on your own, you   can still hire a real estate agent without them being as much on a   strain on your pockets as they would like to be. Unlike going the FSBO   route, there are a number of different approaches to chopping down agent   costs, and they are listed below:</p>
<h3>
  Hardball Option Number 1: Negotiate their Commission</h3>
<p>
  Real estate agents won't tell you this, but their commission is very   negotiable. They are looking to move as many properties as they can to   stack their commission, so if lowering their commission on one home will   help them make a faster profit, they might be willing to cut their   commission in half. That means just being a little pushy could chop the   exorbitant expense of $12,000 to a much more manageable $6,000.</p>
<h3>
  Hardball Option Number 2: Offer to do Part of the Agent's Job—For a Price</h3>
<p>
  Some real estate agents are going to flat out say no to your hardball   negotiation tactics, but if you offer them a hybrid deal in which you   employ some of those FSBO strategies to do some of the legwork, you   should be able to convince the agent to give you a steep discount on   their commission.</p>
<h3>
  Hardball Option Number 3: Request an MLS Listing for a Relatively Modest Fee</h3>
<p>
  A major advantage that real estate agents have in marketing homes is   what is known as the multiple listing service (MLS). Here, agents are   able to market homes to buyer's agents and licensed investors. You have   to have a real estate license to access the MLS, so paying an agent to   list your home on the MLS and do nothing else will give you huge   negotiating power with regards to their commission; maybe you could even   turn it into a low fee, since they won't be doing any actual work. This   is a great deal for an agent with lots of properties on his plate.</p>
<h3>
  Never Forget Your Negotiating Power</h3>
<p>
  Always remember, an agent needs people like you to survive, so if you   feel like you have to use one, don't be afraid to remind him or her of   that when negotiating their commission. It will go a long way towards   saving you thousands.</p>
<h2>
  Route Number 3: Find an Investor</h2>
<p>
  Real estate investors are the real money guys of the industry; they   close quickly and almost always pay cold hard cash. Let's look at some   ways that you can find an investor to buy your home:</p>
<h3>
  Investor Option Number 1: Stop by an Auction or Three</h3>
<p>
  Many real estate investors find their properties by bidding at auctions   and sheriff sales. Stopping by one of these events and doing a little   networking may lead you right into the lap of your buyer. Be wary,   though. These guys don't like to pay full price for properties and may   try to talk you down.</p>
<h3>
  Investor Option Number 2: Call &ldquo;For Rent&rdquo; Signs</h3>
<p>
  Oftentimes, a landlord is looking to expand his or her territory; a very   welcome call from someone looking to sell a property ASAP might be just   what they're looking for. Like other investors, they do often seek   discounted properties, but because they plan on getting long-term value   out of the investment in your house, they are easier to negotiate with.</p>
<h3>
  Investor Option Number 3: Call those &ldquo;We Buy Houses Cash&rdquo; Signs</h3>
<p>
  The cheap quality of these signs may give off a sketchy vibe, but the   people that post them are legitimate investors trying to earn a living   like everyone else. These investors, called wholesalers, serve as bird   dogs for cash investors, and only get paid when they close a deal   between you and the buyer. Because they only get paid when you do, you   can rest assured that they will put their blood, sweat and tears into   closing a deal for you—usually overnight. Keep in mind, though, that   they work for the cash investor, not you; they will only close a deal if   a discount is involved, but if the home is costing you money just by   holding it, this is a great option.</p>
<h3>
  Investors Work Fast at Price</h3>
<p>
  Dealing with investors is usually the quickest route to closing your   home sale; this is a definite boon if you need to sell your property   yesterday, but just keep in mind that they are in the business of making   money off of your house, and they will rarely offer you fair market   value.</p>
<h2>
  Route Number Four: Don't Sell Your House</h2>
<p>
  So, you're having trouble moving your house; maybe you just haven't   gotten around to it. That's okay, because you don't necessarily have to   sell. Becoming a landlord is not all that difficult, and it can enable   you to move while keeping a steady stream of cash flowing into your   pockets; many landlords are able to rent for prices at around fifty   percent more than their mortgage, so if you had a $2000 mortgage, for   example, you could be sitting on a potential $1000 monthly income. Below   are a couple of extremely simple steps that will get you on your way to   becoming a landlord:</p>
<h3>
  Step Number One: Call a Lawyer</h3>
<p>
  Like everything else involving contracts, the small fee of hiring a   lawyer to help you will go a long way in protecting your assets. Call a   local law firm and see how they can guide you in the right direction</p>
<h3>
  Step Number Two: Figure out what Kind of Landlord you want to be</h3>
<ul class="arrow">
  <li><strong>
    Property-Managed Landlords</strong>
<ul class="arrow">
      <li> The right property management firm will literally enable you to sit   back and collect a check every month. They handle advertisements, rent   collection, and some even cover minor maintenance. The only time that   they'll call you is if there is a major repair that needs to be done.   They do all of this for a small percentage of your rent income.</li>
    </ul>
  </li>
  <li><strong>Do it Yourself Landlords</strong>
<ul class="arrow">
      <li> Just as the title suggests, a DIY landlord does all of the legwork her   or himself. This is great if you have the time and patience; what's   more, if you find the right tenant, you will be able to sit back and   collect a check as if you had hired a property management firm in the   first place. Be wary, though. DIY landlording is rarely a walk in the   park—you will most likely have to deal with late rent payments, damages,   and in extreme cases, evictions and squatters.</li>
      <li>As a DIY landlord, you are going to have to do everything, including   the legal paperwork; this is where that lawyer you hired will really   come in handy. Make sure your home is up to code, and your paperwork is   sound, and you should be protected from legal issues.</li>
      <li>Much like an FSBO seller, DIY landlords have to market their   properties themselves, so stop by those same sites we mentioned earlier:   Zillow, Trulia, EBay, Craigslist, ect. Don't forget to post your own   &ldquo;For Rent&rdquo; signs either.</li>
    </ul>
  </li>
</ul>
<h3>
  Being a Landlord Usually Has More Cash Potential Than Selling</h3>
<p>
  Being a landlord isn't always an easy job, but if you're worried that   you won't be able to sell your home for the desired returns, or you can   see your house as a long-term investment, then maybe the path of a   landlord is the right one for you.</p>
<h2>
  Now That You Know How to Save, You Can Sell (or Not) </h2>
<p>
  Regardless of which route you choose, if you use the approaches   mentioned in this guide, you are sure to be successful in selling or   renting your home. Just be sure to use the steps that we have outlined   to put together a plan, take action, and take heart if things don't go   your way the first time around; do this, and you will be well on your   way to saving money on the sale (or rental) of your home!</p>
