				<div class="main-heading"><h1> Getting a Mortgage in Tornado Alley </h1>
                </div>
                    <ul id="breadcrumbs">
                        <li><a href="https://www.mortgagecalculator.biz/">Home</a></li>
                        <li><a href="https://www.mortgagecalculator.biz/resources/">Resources</a></li>
                        <li><a href="https://mortgagecalculator.biz/resources/natural-disasters.php">Natural Disasters</a></li>
                        <li>Tornadoes</li> 
                  </ul>
                </div>
<div class="bottom-section">
<p>Living in <a href="http://www.ncdc.noaa.gov/climate-information/extreme-events/us-tornado-climatology/tornado-alley">Tornado Alley</a> doesn't make it harder to get a mortgage, but it  does make some of the costs of home ownership—like insurance and maintenance—a  little higher.</p>
<h2>Where is Tornado Alley?</h2>
<p>Tornado Alley is a region in the central United States, mostly located in the  Great Plains states. Traditionally, the definition includes Nebraska, Kansas,  Oklahoma, and Northern Texas.</p>
<p>This definition of Tornado Alley is somewhat misleading, however, since it  implies that most tornadoes occur in these states. In fact, there are other  regions in the US where tornadoes happen either more frequently—such as  Florida—or at least as frequently, like Louisiana, Iowa, Illinois, and Indiana.  So, while those states that are traditionally counted as being part of Tornado  Alley do in fact have a higher-than-average incidence of tornadoes, there are  plenty of other states that have at least the same level of risk, and if you  define &quot;Tornado Alley&quot; according to the frequency of tornadoes, it  includes more states than the traditional definition implies. No matter how you  define Tornado Alley, however, it's clear that the high-risk tornado zone isn't  confined to the Midwest; it extends across much of the east as well.</p>
<p><img src="../img/tornado.jpg" width="630" height="442" alt="Tornado." /></p>
<h2>Are Mortgage Requirements or Eligibility Affected by Tornado Risk?</h2>
<p>Some natural disasters—like floods and earthquakes—affect mortgage requirements  because damage caused by these events aren't covered by standard homeowner's  insurance policies. In these cases, mortgage lenders tend to stipulate that  home-buyers must purchase additional insurance in order to be eligible for a  mortgage.</p>
<p>This is not the case with damage caused by tornadoes, because wind and hail  damage are covered by standard homeowner's insurance. Therefore, your mortgage  eligibility is just the same if you live in Tornado Alley as it would be in a  state where the risk of tornado damage is lower.</p>
<p>Other factors that are tangentially related to mortgage eligibility—like  insurance premium costs, and building code requirements—might be affected,  however. For example, homeowner's insurance usually costs more in states where  the tornado risk is higher, since wind and hail damage is more likely to occur  in those areas. As a result, the overall cost of buying a home is slightly  higher than it might be elsewhere, since lenders require that homeowners  purchase standard insurance to protect their investment.</p>
<h2>Advance Payment of Insurance Premiums Typically Required at Closing</h2>
<p>No matter what type of homeowner's insurance you're purchasing, you're  typically required to pay a certain number of payments in advance, typically at  closing. The costs of the advance payments are rolled into the closing costs  figure, and the money is deposited in the home's escrow account.</p>
<h2>Where Are Tornadoes Most Likely to Occur?</h2>
<p><img src="https://www.mortgagecalculator.biz/img/tornado-risk.png" width="879" height="659" alt="Risk of Tornado Damage Across the United States."></p>
<h2>What Happens to Your Mortgage in the Event of a Tornado?</h2>
<p>When a tornado hits and causes damage to your home, your options differ  depending on the extent of the damage. Providing the house sustains a  relatively low level of damage, lenders are happy to leave the mortgage as-is,  and the homeowner can use the insurance payout on repairs.</p>
<p>When the damage is extensive enough that the home is completely destroyed  and needs to be rebuilt, most standard mortgage contracts say that insurance  payouts should be used to rebuild the house. Federal guidelines confirm this,  but some mortgage lenders have been known to put pressure on the homeowner to  use the insurance money to pay off the mortgage.</p>
<p>In the aftermath of particularly devastating tornadoes, some mortgage servicers  even withhold insurance checks to try and force homeowners to use the money to  pay the mortgage, instead of rebuilding. This occurred, for example, after the  2013 tornadoes that caused $2 billion worth of damage in Oklahoma. Most  homeowners simply aren't aware that insurance checks are typically made out to  both the homeowner <em>and</em> the mortgage servicer (and that the servicer  isn't always the same institution that originated the loan). This means that  for a homeowner to actually be able to use the money, the check needs to be  endorsed by the servicer. As a result, many homeowners end up under  considerable pressure from the servicer to pay off the mortgage rather than  rebuilding, even though they are entitled to rebuild if they wish.</p>
<h2>Summary</h2>
<ul class="arrow">
  <li>In many states in the central and eastern parts  of the US, there are regions which are at risk of tornado and heavy wind damage</li>
  <li>Mortgage eligibility isn't directly affected by  tornado risk</li>
  <li>In high-risk states there are more stringent  building code requirements that help ensure homes are built to withstand  serious wind and hail damage</li>
  <li>Wind and hail damage—including that caused by  tornadoes—is usually covered by a standard homeowner's insurance policy</li>
  <li>A certain number of homeowner's insurance  payments usually have to be paid when you close on a house, and further  payments are paid into an escrow account</li>
  <li>When a home is destroyed by a tornado,  homeowners can choose to rebuild or pay off the mortgage</li>
</ul>                     
