				<div class="main-heading"><h1>Getting Mortgages for Properties in Flood Zones </h1>
                </div>
                    <ul id="breadcrumbs">
                        <li><a href="https://www.mortgagecalculator.biz/">Home</a></li>
                        <li><a href="https://www.mortgagecalculator.biz/resources/">Resources</a></li>
                        <li><a href="https://mortgagecalculator.biz/resources/natural-disasters.php">Natural Disasters</a></li>
                        <li>Flooding</li> 
                  </ul>
                </div>
<div class="bottom-section">
<p>Buying a house that's located in a flood zone won't affect your mortgage  eligibility, but indirectly it is going to make the purchase more costly. If  your new home is in a flood zone, your lender is likely going to require that  you buy flood insurance, with an average annual premium cost of around $650, according  to the National Flood Insurance Program.</p>
<h2>Where are the Major Flood Risk Areas?</h2>
<p>Some areas of the country are naturally higher-risk than others, and while  some of these areas are obvious—like Manhattan, New Orleans, and southern  Florida—there are many other locations where the risk isn't immediately  apparent. It's also important to note that areas that are not currently at risk  may become so over the next several decades, as a result of climate change that  may alter weather patterns and sea levels in ways that increase flood risks.  Several states, including Georgia, North and South Carolina, Virginia, and  Massachusetts, have communities that may be affected in this way in the future.</p>
<p>If you're not sure whether you live in a flood risk area, there's an easy  way to find out. At the FEMA website, homeowners can <a href="https://msc.fema.gov/portal">access flood maps</a> that show the level of  flood risk in their location. Currently, there are over 20,000 communities that  are designated by FEMA as &ldquo;flood zones.&rdquo; Homeowners in these communities are  eligible for the <a href="http://www.fema.gov/national-flood-insurance-program">National Flood Insurance Program</a>.</p>
<p><img src="../img/flooding.jpg" width="630" height="449" alt="Flooding." /></p>
<h2>Why Some Mortgage Lenders Require Flood Insurance</h2>
<p>When you get a mortgage, you're taking out a loan that uses your  newly-purchased home as collateral. Mortgage lenders naturally require that  your collateral be insured, because they have a significant financial interest  in it—especially at the beginning of the loan term, when the lender usually has  a larger financial interest in the property than the owner does.</p>
<p>If you live in a high-risk flood zone, you are almost always required to  purchase separate flood insurance that covers the property for the entire  lifetime of the mortgage. A standard homeowner's policy doesn't cover flood  damage, because flooding isn't a significant risk in all parts of the country,  so there's no need for it to be part of a standard policy. Note that flood  damage is by definition different from water damage—a house can incur water  damage in the absence of a flood, because water damage can be caused by events  unrelated to flooding. For example, water damage that results from a leaking  tap doesn't count as flood damage and isn't covered by the NFIP.</p>
<h2>When does Your Mortgage Require Flood Insurance?</h2>
<p>In the case of federally regulated or insured lenders, flood insurance is  mandatory for homeowners in high-risk areas. These federal-associated lenders  include institutions like Fannie Mae (the Federal National Mortgage  Association) and Freddie Mac (the Federal Home Loan Mortgage Corporation), the Federal  Housing Administration, and the Department of Veterans Affairs. So, if you are  applying for a mortgage from one of these federally regulated lenders, and the  property is in a high-risk flood zone, then you are required to have separate  flood insurance in order to qualify for the mortgage.</p>
<p>Not all lenders require that new homeowners purchase separate flood  insurance. For lenders that are not federally-regulated or insured, there's no  mandate that stipulates they must require flood insurance. In these cases it's  up to the lender's discretion, and therefore up to the home-buyer to find out  from potential mortgage lenders whether the insurance is necessary.</p>
<p>It's important to note, however, that properties in low-risk and  moderate-risk areas are not immune to flooding and flood damage. According to  the NFIP, nearly <a href="https://www.floodsmart.gov/floodsmart/pages/about/when_insurance_is_required.jsp">one-quarter  of its claims</a> come from homeowners who live <em>outside</em> high-risk  flood areas. Because of this, the NFIP recommends flood insurance to all  homeowners, and many lenders require homeowners purchase flood insurance even  when the property is not in a high-risk area.</p>
<h2>Where is Flooding Most Likely?</h2>
<p><img src="https://www.mortgagecalculator.biz/img/flooding-risk.png" width="879" height="659" alt="Flooding Risk Across the United States."></p>
<h2>Where are Hurricane Storm Surges Most Likely?</h2>
<p><img src="https://www.mortgagecalculator.biz/img/hurricane-risk.png" width="879" height="659" alt="Hurricane Risk Across the United States."></p>
<h2>Paying for Flood Insurance Is Required at Closing</h2>
<p>If your flood insurance is legally mandated—that is, if you're in a  high-risk zone and your mortgage comes from a federal-associated lender—then  you'll probably be required to make a certain number of premium payments up  front. When this is the case, those payments must usually be made at or before  closing time, and an entire year's worth must be paid at this time.</p>
<p>The upside is, that providing the first year's worth of payments are made at  or before closing, the standard 30-day wait for the policy to come into effect  doesn't apply. If the premium is paid at or before closing, the flood insurance  is in effect immediately at the time of closing.</p>
<h2>Summary</h2>
<ul class="arrow">
  <li>Homeowners in eligible areas can purchase flood  insurance from the National Flood Insurance Program</li>
  <li>If your mortgage lender is federally-regulated  or insured, you must buy flood insurance if your property is in a high-risk  flood zone</li>
  <li>If your lender isn't federally-associated, or  you're not in a high-risk zone, you may not be required to buy flood insurance</li>
  <li>The NFIP strongly recommends homeowners buy  flood insurance even if they're not in high-risk zones, because flood damage  occurs even in low and moderate-risk zones</li>
  <li>In most cases a homeowner is required to pay the  first year's worth of premium payments at closing</li>
</ul>
                               
