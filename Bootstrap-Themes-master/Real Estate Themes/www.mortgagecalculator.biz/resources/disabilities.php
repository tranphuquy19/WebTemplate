				<div class="main-heading"><h1>Securing a Home Mortgage Loan with a Disability</h1>
</div>
                    <ul id="breadcrumbs">
                        <li><a href="https://www.mortgagecalculator.biz/">Home</a></li>
                        <li><a href="https://www.mortgagecalculator.biz/resources/">Resources</a></li>
                        <li>Guide for People With Disabilities</li>
                    </ul>
                </div>  			
                <div class="bottom-section">
                
                       
 <p>If you have a disability and wish to buy your own home, this guide is  designed to help you and your family during the process. It will help you  confirm that buying your own property is the right decision for you and guide  you through the steps of buying a home. You will also learn about the financial  assistance available when you have a disability to help you secure home  ownership.</p>
<h2>Pros and Cons of  Home Ownership</h2>
<p>While more than 40 million people in the US live with a disability, just a  small proportion benefit from home ownership, which promotes greater  independence, community involvement and a sense of control.</p>
<h3>Independence and  Connection to Your Community</h3>
<p>The majority of people with disabilities live in group homes, residential  care facilities or in their parents' home, where someone else is in charge.  However, even if you require a degree of assistance, independence is important  and having your own home allows you to take control and responsibility of your  life. Beyond independence home ownership allows you to feel part of the  community, as you connect with your neighbors and feel a sense of belonging.</p>
<h3>Responsibility and  Assistance</h3>
<p>Financial planning and investing in a home lets you take responsibility for  your future, so not only are you prepared, but you feel in control and  accomplished. If you need assistance, you can still receive this in your own  home and are at the advantage that it can be tailored to your needs and  routines rather than that of an institution.</p>
<h3>A Big Commitment</h3>
<p>Buying your own home is a significant move, particularly when you have  disabilities, owing to the greater independence and responsibility required,  and the need to make sure that any assistance and services you need are  available in your neighborhood. Home ownerships is also more expensive, though  financial assistance can help meet your needs.<br>
  As there may be additional advantages and disadvantages of home ownership  for your given circumstances, you may wish to discuss your decision with a  housing counselor. They are based locally in all US states and offer a  dedicated service to people with disabilities. You can use your Yellow Pages or  telephone directory to locate a housing counselor near you.</p>
<h2>Learn About the  Process of Buying a Home</h2>
<h3>Paying for a Home</h3>
<p>It is typical to take out a mortgage loan from a bank to borrow money to pay  for your home if you do not have sufficient funds to pay the asking price. A  mortgage loan works as follows:</p>
<ul class="arrow">
  <li>When you put in a mortgage application the bank checks       your finances. They assess your annual earnings, predicted expenses and       credit history for unpaid debts. With this information the bank will       either approve or decline your request to borrow money.</li>
  <li>You often need to make a down payment using your own       money, which usually cover 3-10% of the loan's value, though some loans       don't require a down payment.</li>
  <li>Your bank will then set out a monthly payment plan that       allows you to pay back the money over a 15 or 30 year period. This sets       out the principal payment (the overall amount you borrowed) and the interest       rate (the percentage of the payment that covers the bank's admin costs and       profit), as well as monthly ongoing costs such as your mortgage       repayments, taxes, insurance and utility bills.</li>
</ul>
<h3>Making a Mortgage  Loan Application</h3>
<p>A housing counselor can help you approach a bank to let them know you wish  to make a mortgage application to purchase a property. Your bank will then do  the following:</p>
<ul class="arrow">
  <li>Complete a credit check to make sure your income is       sufficient to support a loan.</li>
  <li>Based on your income and outgoings the mortgage officer       will calculate the mortgage you can afford.</li>
  <li>Assist you in applying for the agreed loan.</li>
</ul>
<p>Once the bank approves your mortgage you can start looking for properties  that the value of the loan will cover.</p>
<h3>Putting in an Offer</h3>
<p>A real estate agent will help you find a property that meets your needs and  makes contact with the seller for you. If other people are interested in the  same property as you, an auction takes place and the home is sold to the  highest bidder. Once your home is purchased the real estate agent will assist  you with the necessary paper work.</p>
<h2>Getting Started</h2>
<p>Below is a practical guide for buying a home when you have disabilities.</p>
<h3>1. Get Support</h3>
<p>As buying a property is often a lengthy process that involves various  meetings, decision making and isn't without its frustrations, having family and  friends on board to support you is a good idea.</p>
<h3>2. Choose a Housing  Counselor</h3>
<p>Selecting a local HUD approved housing counselor allows you to receive free  or low-cost advice on your housing options locally, financial assistance  programs and the best solution to fit your practical and financial needs.</p>
<h3>3. Determine Your  Needs and Your Budget</h3>
<p>Establish what you are looking for in a property and what needs you have as  a result of your disability. Your housing counselor can help you with this and  help set a budget that incorporates the additional costs of any assistance you  will require when in your own home.</p>
<h3>4. Get  Pre-Qualified</h3>
<p>Although not always available, approaching a lender to quickly review your  finances and estimate the amount you can borrow is helpful, as this mortgage pre-  qualification allows you to narrow down your home search based on the loan  value.</p>
<h3>5. Make an Offer</h3>
<p>Your housing counselor and real estate agent can help you find a suitable  home on which to make an offer.</p>
<h3>6. Apply for a  Mortgage</h3>
<p>Your housing counselor can help you apply for a mortgage. If you have a low  income that does not qualify you for a standard mortgage, they can also guide  you on more favorable mortgage programs and additional financial assistance you  are entitled to.</p>
<h3>7. Arrange a Home  Inspection</h3>
<p>Even if the property you are interested in has already had a home inspection  arranged by the seller, organizing your own not only lets you confirm its  value, but also makes sure that the home is accessible for your needs.</p>
<h3>8. Close the Deal</h3>
<p>Before the property ownership is transferred to you on an agreed date, the  lender's closing attorney will contact you and explain how closing the deal  will take place and what you need to bring with you.</p>
<h2>Understand Your  Rights</h2>
<p>There are laws and regulations to protect anyone with a disability during  the home-buying process, so it is important you know your rights.</p>
<h3>Disability Rights  in Housing</h3>
<p>With a confirmed disability this <a href="http://portal.hud.gov/hudportal/HUD?src=/program_offices/fair_housing_equal_opp/disabilities/inhousing">federal  law</a> makes sure that you:</p>
<ul class="arrow">
  <li>Cannot be discriminated against, so you cannot face       different qualification criteria, terms or costs than anyone else.</li>
  <li>Are accommodated by housing providers, so their       services, policies and practices are accessible to you.</li>
  <li>Can make reasonable adjustments to a property to make       it safer or aid accessibility, such as grab rails or a lower threshold.</li>
</ul>
<h3>The Fair Housing  Act</h3>
<p>As well as making sure that your disability does not stand in the way of you  obtaining housing, multifamily residences constructed after March 13 1991 must  meet various <a href="http://www.justice.gov/crt/about/hce/title8.php">accessibility  requirements</a>. This includes an accessible entrance, through routes, common  rooms, kitchens and bathrooms, as well as doors that are wide enough for  wheelchair users.</p>
<h3>Americans with  Disabilities Act</h3>
<p>Although this law applies to various situations and prohibits discrimination  on the ground of disability, it is another <a href="http://www.ada.gov/">law  that protects you</a> during the home-buying process.</p>
<h2>Gain Financial  Assistance</h2>
<p>If you have a disability and do not have sufficient income to access a  traditional mortgage, financial programs can help make the dream of home  ownership a reality.</p>
<h3>SSI Disability  Programs</h3>
<p>To be eligible for the Supplemental Security Income Program, which responds  to financial need, you need to meet the criteria outlined <a href="http://www.ssa.gov/ssi/text-eligibility-ussi.htm">here</a>. Their website  also provides application forms that you can download and complete after  arranging a meeting to assess your requirements and eligibility for support. If  your application is approved this may offer you a sufficiently stable income to  access a government-backed mortgage and affordable housing.</p>
<h3>Home Ownership  Voucher Program</h3>
<p>This Government housing program allows families with moderate and  low-incomes where members also have a disability to buy or rent a property.  Eligibility criteria for the Housing and Urban Development's voucher scheme are  detailed <a href="http://portal.hud.gov/hudportal/HUD?src=/topics/housing_choice_voucher_program_section_8">here</a>.  As not all Public Housing Agencies provide home ownership as an option, you  will need to research whether this is the case in your local area and even then  you will need to find your own property. If eligible you will receive a monthly  payment to cover your mortgage costs.</p>
<h3>Habitat for  Humanity</h3>
<p>This non-profit organization <a href="http://www.habitat.org/where-we-build/united-states">builds accessible  accommodation</a> for people in financial need and then provides favorable  mortgages to allow their purchase, though you must still prove that you can  meet repayments. As the scheme relies heavily on volunteers you will be asked  to make a contribution to their house building efforts in whatever way you can  through sweat equity.</p>
<h3>Additional  Resources</h3>
<ul class="arrow">
<li><a href="http://www.nod.org/">National Organization on Disability</a>:  Improving access to housing is just one of the schemes that NOD runs.</li>
<li><a href="http://www.realeconomicimpact.org/">National Disability Institute</a>:  As well as providing housing assistance, NDI has links with various banks and  investment businesses.</li>
<li><a href="http://www.aapd.com/">American Association for People with  Disabilities</a>: One of AAPD's aims is to ensure economic security for anyone  living with a disability, which includes greater access to financial bene</li>
</ul>

                    
