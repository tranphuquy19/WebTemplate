				<div class="main-heading"><h1>Divorcee’s Guide to Mortgages &amp; Home Buying</h1>
</div>
                    <ul id="breadcrumbs">
                        <li><a href="https://www.mortgagecalculator.biz/">Home</a></li>
                        <li><a href="https://www.mortgagecalculator.biz/resources/">Resources</a></li>
                        <li>Divorcee’s Guide</li>
                    </ul>
                </div>  			
                <div class="bottom-section">
   
<p><img src="../img/white-roses-and-rings.jpg" width="800" height="530" alt="White roses and wedding bands."></p>
<p>As if the emotional challenges of divorce aren't enough,  homeowners must also decide what is to become of the house they once shared  with their soon-to-be-ex-spouse. It's vital to not only know what your options  are, but to truly understand them so you can make an informed choice that's  right for your situation. The following guide will discuss your options  surrounding your current mortgage as well as those for buying a new home  following a divorce. Note that it cannot substitute the advice of a licensed  divorce attorney, especially as there may be state-specific laws to consider in  your area.</p>
  <p>Dealing with the division of property can be a particularly  heartbreaking aspect of a divorce, so give yourself time to weigh the following  choices carefully. Don't be afraid to ask your attorney questions about how  your specific circumstances apply — the more confident you feel in your  decisions, the easier it will be to let go and move forward.</p>
<h2>Mortgage: Assumption, Refinancing &amp; the Role of Quitclaim Deeds</h2>
<p>Part of what makes handling the mortgage in a divorce so  complicated is that there are a number of factors to consider:</p>
<ul class="arrow">
  <li>Your  income &amp; your spouse's income</li>
  <li>Whether  one spouse wants to continue living in the home</li>
  <li>Current  market value of the home</li>
  <li><a href="https://www.mortgagecalculator.biz/c/balance.php">Equity</a> on the home</li>
</ul>
<p>Addressing whether you or your spouse would like to keep the  home is a good first step, and often depends on whether you have children  together. Saying goodbye to the place where you spent your life together is  emotional, but it's important not to lose sight of what's realistic for your  finances. The fact is, even if it's your spouse who stays in the residence, you  could still be held liable on the <a href="http://time.com/money/4274952/mortgage-in-a-divorce/">mortgage</a> long after your divorce is finalized.</p>
  <p>If you decide you'd like to stay and your spouse agrees, you  have two main options for the mortgage: you can try to assume it, or you can  apply to refinance it. If you assume the mortgage, you'll be responsible for  the entire payment each month. In order to remove your ex's name from the  document, you'll have to file a request with your lender. Keep in mind that in  many instances, it's incredibly difficult to make a full mortgage payment on  only one salary — and even if you're willing to try, your lender might not be  comfortable taking the risk and deny the request. You may be able to <a href="http://money.usnews.com/money/personal-finance/articles/2015/04/09/7-things-to-remember-before-renting-out-a-room">rent out</a> a room or section of your house as a  way to help make the mortgage payment each month, but you'll have to inquire  about property laws in your area.</p>
  <p>If you apply to refinance the mortgage, essentially you'll  re-apply for the same loan but under your income alone. Unfortunately,  refinancing comes with quite a few costs and fees and could affect the amount  of interest you end up paying. The lender will want to see that you have a  strong credit history and sufficient income to make a monthly mortgage payment,  which will be smaller than the one you had with your ex. The more equity you  have in your home already, the better.</p>
<p>&nbsp;</p>
<p><img src="../img/modern-home.jpg" width="800" height="247" alt="Modern Home."></p>
  <p>The problem for many couples facing divorce is that their  home has gone down in value since it was purchased. If the house has become <a href="http://homeguides.sfgate.com/legal-questions-mortgages-involving-divorce-9674.html">worth less</a> than the balance of the mortgage,  lenders won't be eager to make changes. Even in the event of a divorce, they <a href="http://info.legalzoom.com/released-mortgage-divorce-26224.html">aren't required</a> to release anyone on your  mortgage from liability — your ex would be held financially responsible if you  defaulted on a payment.</p>
  <p>In some divorce agreements, a <a href="https://en.wikipedia.org/wiki/Quitclaim_deed">quitclaim deed</a> is used as a way to free the  spouse not staying in the home of any and all interest in the property. It  removes their name from the deed, but not, however, from the mortgage. For  example, say you stay in the home following your divorce but weren't able to  refinance the mortgage. You make some life changes to cover the entire mortgage  payment, and your ex wants their name officially off the title. Signing a  quitclaim deed would mean that, though they'd still be held liable if you missed  a mortgage payment, they <em>wouldn't</em> have any financial responsibility if a major pipe burst in the home. Quitclaim  deeds can be somewhat risky for both parties, so heed the advice of your  divorce attorney with care.</p>
<h2>The Safest, but Saddest Option: Selling the Home</h2>
<p>In a majority of cases, the simplest option that benefits  both parties equally is selling the home, paying off the debt balance, and  splitting any profit. Of course, this is another area that depends greatly on  the current market value of your home — especially if it's taken a fall. Some  couples opt to hold onto their property a little longer in the hopes that the  local housing market will bounce back. The question of what happens to the home  in the meantime can get tricky, however, because both spouses are responsible  for making the mortgage payments until the house is sold.</p>
  <p>Some couples may choose to continue living in the house  together (but separately) for a few months, though this isn't a good option in  the case of particularly painful divorces. Other couples will rent out their  house while waiting out market conditions. This can be a smart way to keep up  with the mortgage payments on the shared property while still allowing you to  use your personal income to find and pay for a new living situation. (More on  that in a moment.) You must remember, though, that you'll share landlord  responsibilities during this time and will need to have a positive working  relationship with your ex. For those who can afford it, sometimes taking a loss  on the house is the best step to cut all ties.</p>
  <p><img src="../img/home-sold.jpg" width="800" height="532" alt="Home Sold."></p>
<h2>Moving Forward: Your Options for Home Buying Following Divorce</h2>
<p>The truth is, your exact options for home buying will vary  depending on your circumstances. If you're still tied to your old mortgage, you  likely won't be able to take out a loan for a new home unless you can prove  that you can afford two mortgage payments on only your income. However, if you  are able to prove that you're renting out the home, your former spouse has  assumed (and been consistently paying) the mortgage payments, or the home is in  the final stages of being sold, you may be able to get a new home loan.</p>
<p>In general, it's best to avoid buying new property until  after the <a href="http://money.usnews.com/money/personal-finance/articles/2014/10/29/how-to-get-a-mortgage-after-a-divorce">divorce</a> is finalized. Legal expenses will  already be burning a hole in your wallet, so be conscientious in the meantime.  Some divorcees find it cathartic to at least begin house hunting once they've  separated from their ex, so feel free to get out there and see what the market  is like. It might be in your best interest to keep your impending divorce to  yourself, however, at least until all the paperwork is signed and filed. Some  homeowners or real estate agents might feel hesitant if they think you'll be  tied up in divorce court or facing steep legal fees.</p>
  <p>If you decide to rent property while you're working out the  terms of your divorce, your lingering mortgage will still show up on a credit  report. It won't necessarily destroy <em>any</em> chances you have of finding a place, but it can be a red flag in certain  circumstances. Landlords will be concerned about your need to make both a  mortgage payment and a rent payment. They might inquire further about your  income or other finances, or ask to see proof that your spouse has assumed the  mortgage.</p>
  <h2>The Importance of Documentation</h2>
  <p><img src="../img/legal-documents.jpg" width="800" height="476" alt="Legal Documents."></p>
<p>Whatever mortgage and property decisions you make with your  ex, be sure that everything is spelled out clearly and explicitly in your  divorce agreement. If your spouse will be assuming the residence and taking  over mortgage payments, specify the exact date it will take effect and the  amount of the monthly payment. Some choose to have some kind of penalty should  one spouse default on a shared mortgage payment; after all, <em>both</em> parties will take a hit to their  credit if there's any kind of lapse in payment.</p>
  <p>Keep detailed records of every step you take. If you'll be  selling, make sure to have clear documentation of every detail you and your ex  have agreed upon. This can be especially important if you'll be taking a loss  on the house — you'll want it in writing that each of you will need to bring  the split balance of what you owe to the closing.</p>
  <p>Avoid having any conversations about the mortgage or your  home with your ex when your divorce attorney is absent. Even conversations that  start out completely innocent can escalate into a bitter argument, and you  don't want to lose any progress you've made. If you share custody and have to  regularly see each other, keep your conversations focused on the children.  Don't discuss any of your legal woes with your kids, even if they ask. Even  inadvertently &ldquo;bringing the kids into it&rdquo; can cause major issues between a  divorcing couple, especially when they are trying to cope with their separation  of property.</p>
  <p>It might feel overwhelming now, but understanding your  mortgage and home buying options is half the battle. Talk to your divorce  attorney about what state laws may come into play in your situation, and make  sure you walk away from each meeting feeling confident in your next step. In  time, you'll be able to take those final steps forward into a new home,  mortgage, and life.</p>                    
