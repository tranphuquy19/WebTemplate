				<div class="main-heading"><h1>How Mortgages Are Affected in Disaster-Prone Areas</h1>
</div>
                    <ul id="breadcrumbs">
                        <li><a href="https://www.mortgagecalculator.biz/">Home</a></li>
                        <li><a href="https://www.mortgagecalculator.biz/resources/">Resources</a></li>
                        <li>Homes in Disaster-Prone Areas</li>
                    </ul>
                </div>  			
                <div class="bottom-section">
<p><img src="https://www.mortgagecalculator.biz/img/earthquake-damage.jpg" alt="Earthquake Damage." width="800" height="456" /></p>
<p>Anyone who's ever lived in a region where natural disasters  are prevalent knows all about the physical effect they can have on property, be  it hurricanes, flooding, earthquakes, or any other destructive force of nature.  What many people don't realize, however, is that disaster-prone areas often see  increased housing costs, as well — and it can even impact mortgage rates. From  higher insurance to damages and repairs to increased property taxes, there are  several factors that can affect your mortgage rate.</p>
<p>Shopping for a new home requires a lot of research and  thorough understanding of the housing market in your area. You have to examine  the individual prices of homes as well as how those values have changed over  time. In particularly weather-volatile areas, you'll want to take note of  trends following major disasters: how often they occur, what kinds of property  damage they cause, and their overall effect on home values in the area. Be  specific as much as possible. For instance, does one neighborhood see more  flood risk than others? Are older homes at any kind of increased risk because  of outdated materials or design? Did homes with certain modifications fare  better against extreme conditions? Keeping these kinds of questions in mind  will help you shop smarter when it comes to both quality and price of local  homes.</p>
<p>Further, it's important to consider the major additional  expenses of homeownership like property tax and insurance. Taxes in  disaster-prone areas <a href="http://www.govtech.com/em/disaster/Hurricane-Matthew-clean-up-costs-could-lead-to-a-county-property-tax-increase.html">sometimes increase</a> following a major event, so  find out how city and state taxes have fluctuated over the last 10-20 years.  When it comes to <a href="http://www.businessinsider.com/how-the-recent-natural-disasters-will-affect-your-insurance-rates-2012-7">insurance</a>, rates can rise as a direct result  of a natural disaster, driving up monthly costs at a time when many families  will already be facing major home repairs. Also, many people don't realize that <a href="http://time.com/money/4528210/what-you-need-to-know-about-flood-insurance/">flood insurance</a> is <em>not</em> included in most homeowner insurance policies, but must be  purchased separately — and carefully.<br />
  <img src="https://www.mortgagecalculator.biz/img/flooded-city.jpg" alt="Flooded City." width="800" height="472" border="0" /></p>
<p>It's important to keep these potentially fluctuating costs  in mind. Even if they're currently sitting at an expected rate, it's impossible  to say what the future will bring. For instance, if your property taxes are at  an all-time low when you buy, it isn't necessarily the perfect opportunity to  buy a more expensive house at a higher mortgage rate. If a major natural  disaster were to strike your neighborhood six years down the road, you'll  likely see insurance and property tax rates spike. The higher mortgage payment  that was manageable the first six years could really come back to bite you,  especially if your home incurs value-decreasing damage.</p>
<p>When it comes to your mortgage rate, there are a few  considerations to keep in mind. First, if the cost of living (including  handling the effects of natural disasters) tends to be high in your area,  mortgage rates will already be somewhat steep. Not only are lenders likely to  be cautious about investing in a home that's subject to dangerous conditions  and damage, it's also safe to assume they'll take advantage of the current  market. Simply put, if insurance and taxes are usually on the more expensive  side, lenders may see the opportunity to keep their own rates high since  homebuyers will already be expecting to pay more.</p>
<p><img src="https://www.mortgagecalculator.biz/img/hurricane-floods.jpg" alt="Hurricane Floods." width="800" height="600" border="0" /></p>
<p>Next, you should consider the potential damage your home  could face. You might see a fantastic deal on a large home, but if it's in the  flooding zone of a coastal region then it's a major liability for both you and  a lender. (And don't forget: flood insurance is an extra premium to pay!) If  your home is seriously damaged in a flood, it could decrease the value even  after repairs are made. In addition to the financial burden of repairs and  preventative modifications, you'll be paying the same mortgage rate on a house  that's now worth less money. Additionally, if other homes in the area fall in  value, your home will have an even more difficult time getting its value back  up to the original purchase price.</p>
<p>For example, suppose you purchase a house in southern  California for $200,000 and have a monthly mortgage payment of $1,000. Ten  years into the future, you've been making your payments and have built solid <a href="https://www.mortgagecalculator.biz/c/balance.php">equity</a> in your home. An earthquake strikes,  causing extensive damage to your own home and many in your neighborhood.  Insurance will probably help cover some but not all repairs, <em>and</em> you'll likely see your rates go up.  Property taxes may also rise, depending on how many homes and businesses were  affected and the severity of the damage.</p>
<p><img src="https://www.mortgagecalculator.biz/img/tornado-damage.jpg" alt="Tornado Damage." width="800" height="536" border="0" /></p>
<p>Your mortgage rate won't automatically increase in this kind  of a situation. However, if your home has suffered such severe damage that it's  now only worth $150,000, you're already taking a loss by paying the same $1,000  a month (which was based on a home value of $200,000). Essentially, you'll be  on-track to pay much more for the house than it's actually worth. When the time  comes to sell, you'll either have invested significant amounts of money to  boost the value back up to where it was, or you'll have to take a loss and sell  it for less than you bought it for.</p>
<p>Additionally, if the value of other property in your area  has gone down, it can make selling your home even <em>more</em> difficult. Say that following the earthquake, you realized it  would be less costly to take the loss of selling your home at $150,000 rather  than making all the necessary repairs (while paying increased costs in taxes,  insurance, etc). If others in your area are doing the same, but their houses  are selling for closer to $100,000, you're going to have a lot of trouble  selling your home for $150,000. It can cause a troubling dilemma for homeowners  who are already facing extensive home damage: sell the house at a major loss,  or continue investing a great deal of money into it and hope that in time, the  value and market recover. In the meantime, you'll be paying expensive home  repair costs (and/or modifications to prevent future damage) <em>and </em>steeper insurance premiums <em>and</em> potentially higher taxes <em>and</em> a mortgage that reflects a higher  value than the house is currently worth.</p>
<p>You should also keep in mind that if you do decide to sell  following a natural disaster, you might have trouble when it comes to  purchasing a new home. Even if a lender can see that you are in the process of  selling your old property, they may be hesitant to grant you a new loan until  all is resolved. Even if the house sells quickly, taking a loss on it will hurt  your equity and thus your future borrowing abilities. You might end up facing a  less-than-ideal mortgage rate on a new home — assuming you're able to get one  at all — and ultimately not be much better off financially than you would be in  your damaged home.</p>
<p>This isn't to say that you can never make a wise investment  on a home in a disaster-prone area, but instead that there are many risks to  consider. Ideally, you'll never see <em>any</em> destructive weather or find your house in shambles after a natural disaster,  but if you live somewhere that commonly endures these types of events, you  can't discount their possibility of happening. The more you know about the  risks ahead of time, the more informed a decision you can make when the time  comes to buy.</p>
