    <h2>What This Site Offers</h2>
<p>Welcome to MortgageCalculator.biz - a free resource which was founded in 2013 to help make it simple &amp; easy for anyone to calculate home loan payments and other common personal finance related calculations revolving around the decision of if or when it makes economic sense to buy a house - perhaps one of the most important decisions any of us will make in our lifetimes.</p>
<h3>My Background</h3>
<p><img src="img/big_screen.jpg" alt="Our site." width="200" class="alignright" />Hey, my name is Trey Conway. I am 24 years old, and I grew up in the “peak of good living” Apex, North Carolina. I have always loved the outdoors. Anything that involves hiking with my dog Rusty, rock-climbing, or fishing is where I find my happiness. After reading the book <a href="http://www.amazon.com/Road-Penguin-Modern-Classics/dp/0141182679" target="_blank">“On The Road” by Jack Kerouac</a>, my love for traveling the United States took on a whole new meaning, thus sending me on a cross country tour with my dog for 2 years.    </p>
<p> Upon returning home with many great experiences, (and penniless) it was evident to me that I needed to grow up and head off to college. Growing up in a tech savvy household, my father had always taught me the benefits of being an “egg head.” With that in mind, I enrolled in a few computer classes at Appalachian State University. During one of my business calculus classes, we had discussed how the downfall of Freddie and Fannie had such a devastating impact on our economy.  I had a hard time understanding the mortgage concepts being discussed. </p>

