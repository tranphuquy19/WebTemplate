<h2>Your User Privacy</h2>
<h3>Third Party Ad Networks</h3>
<p>This site did not carry ads when it was launched, but in November of 2017 I partnered with BankRate to include their financial tables on our pages. The rate tables are arguably one of the most useful ad formats across the web, as they add real-time data and interactivity to the site to help people explore the best locally available lending options. When users of our site interact with the rate tables on our site some of the listed rates sources pay BankRate a commission which is shared with our site. We do not control which rate providers appear in the rate tables across the site. BankRate's privacy policy is located at: <strong><font color="#81C363">http://www.bankrate.com/coinfo/privacy.asp</font></strong>. Some of our pages also list content recommendations from MyFinance and their privacy policy is published at <strong><font color="#81C363">https://www.myfinance.com/privacy-policy/</font></strong>.</p>
<p>I do not control cookies nor the privacy policies &amp; practices of third party party networks. I tried to partner with a longstanding trusted network which serves users well, but if you have questions about their policies please consult their privacy policy pages.</p>
<p>If you do not see mortgage rate tables on this website then you might have an ad blocker turned on. </p>
<p>If you see rate tables but do not wish to see them then you can easily block any third party ads using a web browser plug-in like the EFF's <a href="https://www.eff.org/privacybadger" target="_blank">Privacy Badger</a>.</p>
<h3>Web Analytics Services</h3>
<p>Our site has on-site analytics powered by Haveamint and third party analytics tracking from Clicky.com. We do not collect user IP addresses or any other personally identifiable information in Haveamint and Clicky publishes their privacy policy here <strong><font color="#81C363">https://clicky.com/terms/privacy</font></strong>. Analytics data is used to see which pages are popular with users and if users are quickly bouncing from any of our pages. This helps us ensure our calculators are functional across different web browsers and device types as the web keeps changing.</p>
